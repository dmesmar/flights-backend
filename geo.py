def haversine_km(lat1: float, lon1: float, lat2: float, lon2: float) -> float:
    """
    Great-circle distance between two geographic points (Haversine formula).

    Parameters
    ----------
    lat1, lon1 : coordinates of the first point (decimal degrees)
    lat2, lon2 : coordinates of the second point (decimal degrees)

    Returns
    -------
    float : distance in kilometres
    """
    R = 6371.0
    d_lat = math.radians(lat2 - lat1)
    d_lon = math.radians(lon2 - lon1)
    a = (
        math.sin(d_lat / 2) ** 2
        + math.cos(math.radians(lat1))
        * math.cos(math.radians(lat2))
        * math.sin(d_lon / 2) ** 2
    )
    return R * 2 * math.asin(math.sqrt(a))


def get_airport_distance(iata1: str, iata2: str) -> Optional[float]:
    """
    Distance in kilometres between two European airports.

    Returns None if either code is not present in AIRPORT_COORDS.
    """
    c1 = AIRPORT_COORDS.get(iata1)
    c2 = AIRPORT_COORDS.get(iata2)
    if not c1 or not c2:
        return None
    return haversine_km(c1[0], c1[1], c2[0], c2[1])


def get_nearby_airports(iata_code: str, radius_km: float) -> list[dict]:
    """
    Return all European airports within *radius_km* of *iata_code*,
    sorted by ascending distance.

    Parameters
    ----------
    iata_code : IATA code of the origin airport
    radius_km : search radius in kilometres

    Returns
    -------
    list of dicts: [{"iata": str, "distance_km": int}, ...]
    """
    origin = AIRPORT_COORDS.get(iata_code.upper())
    if not origin:
        return []

    results: list[dict] = []
    for other_iata, coords in AIRPORT_COORDS.items():
        if other_iata == iata_code.upper():
            continue
        dist = haversine_km(origin[0], origin[1], coords[0], coords[1])
        if dist <= radius_km:
            results.append({"iata": other_iata, "distance_km": round(dist)})

    return sorted(results, key=lambda x: x["distance_km"])
